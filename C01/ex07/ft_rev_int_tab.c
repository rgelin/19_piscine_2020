void	ft_rev_int_tab(int *tab, int size)
{
	int i;
	int inter;
	int ri;

	i = 0;
	ri = size - 1;
	while (i < size / 2)
	{
		inter = tab[i];
		tab[i] = tab[ri];
		tab[ri] = inter;
		ri--;
		i++;
	}
}
