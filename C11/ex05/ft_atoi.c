#include "ft.h"

int		ft_atoi(char *nb)
{
	int nbr;
	int sign;
	int i;

	sign = 1;
	i = 0;
	nbr = 0;
	while (nb[i] == '-' || nb[i] == '+')
	{
		if (nb[i] == '-')
			sign *= -1;
		i++;
	}
	while (nb[i] >= '0' && nb[i] <= '9')
	{
		nbr = (nbr * 10) + nb[i] - '0';
		i++;
	}
	return (nbr * sign);
}
